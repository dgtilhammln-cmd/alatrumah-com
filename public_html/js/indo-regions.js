function initIndoRegions(config) {
    const provSelect = document.getElementById(config.provSelectId);
    const citySelect = document.getElementById(config.citySelectId);
    const distSelect = config.distSelectId ? document.getElementById(config.distSelectId) : null;
    
    const provInput = config.provInputId ? document.getElementById(config.provInputId) : null;
    const cityInput = config.cityInputId ? document.getElementById(config.cityInputId) : null;
    const distInput = config.distInputId ? document.getElementById(config.distInputId) : null;

    if(!provSelect || !citySelect) return;

    // Populate provinces
    provSelect.innerHTML = '<option value="">Pilih Provinsi</option>';
    INDO_PROVINCES.forEach(p => {
        const opt = document.createElement('option');
        opt.value = p.id;
        opt.text = p.name;
        provSelect.add(opt);
    });

    // On Province Change
    provSelect.addEventListener('change', function() {
        const selectedName = this.options[this.selectedIndex]?.text || '';
        if (provInput) provInput.value = selectedName;

        citySelect.innerHTML = '<option value="">Pilih Kota/Kabupaten</option>';
        if (distSelect) distSelect.innerHTML = '<option value="">Pilih Kota Dulu</option>';
        if (cityInput) cityInput.value = '';
        if (distInput) distInput.value = '';

        if (!this.value) return;

        const cities = INDO_CITIES[this.value] || [];
        cities.forEach(c => {
            const opt = document.createElement('option');
            opt.value = c;
            opt.text = c;
            citySelect.add(opt);
        });
    });

    // On City Change
    citySelect.addEventListener('change', function() {
        const selectedName = this.options[this.selectedIndex]?.text || '';
        if (cityInput) cityInput.value = selectedName;

        if (distSelect) {
            distSelect.innerHTML = '<option value="">Pilih Kecamatan</option>';
            // For district we use city name as value since we don't have full data
            // Just set the value when user types in district field
        }
        if (distInput) distInput.value = '';
    });

    // On District Change
    if (distSelect) {
        distSelect.addEventListener('change', function() {
            const selectedName = this.options[this.selectedIndex]?.text || '';
            if (distInput) distInput.value = selectedName;
        });
    }
}
